package com.aps.form;

public class DonateForm {
	private String donateMoney;
	private String donateName;
	private String email;
	private String phone;
	private String message;
	public String getDonateMoney() {
		return donateMoney;
	}
	public void setDonateMoney(String donateMoney) {
		this.donateMoney = donateMoney;
	}
	public String getDonateName() {
		return donateName;
	}
	public void setDonateName(String donateName) {
		this.donateName = donateName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
}
