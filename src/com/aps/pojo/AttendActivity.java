package com.aps.pojo;

public class AttendActivity {
	private int attendActivityId;
	private String name;
	private String phone;
	private int unhappenId;
	public int getAttendActivityId() {
		return attendActivityId;
	}
	public void setAttendActivityId(int attendActivityId) {
		this.attendActivityId = attendActivityId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getUnhappenId() {
		return unhappenId;
	}
	public void setUnhappenId(int unhappenId) {
		this.unhappenId = unhappenId;
	}
}
